/**
 * Enumeration of different types of disaster relief resources.
 * Used to categorize resources for better organization and filtering.
 */
package com.disa.resource_service.entity;

public enum ResourceType {
    FOOD, // Rice, canned food,....
    WATER, // Bottled water, water tanks
    MEDICINE, // First aid, medications
    EQUIPMENT, // Tents, blankets, tools
    CLOTHING, // Clothes, shoes
    HYGIENE // Soap, sanitizers, toiletries
}